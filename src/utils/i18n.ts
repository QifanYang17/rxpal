const dicts: Record<Lang, TranslationDict> = { zh, en }

export function t(key: string, params?: Record<string, string | number>): string {
  const lang = useStore.getState().lang || 'zh'
  const val = dicts[lang][key] ?? dicts['zh'][key] ?? key

  if (Array.isArray(val)) return val[0] || key

  if (!params) return val
  return val.replace(/\{(\w+)\}/g, (_, k) => String(params[k] ?? `{${k}}`))
}

export function tArray(key: string): string[] {
  const lang = useStore.getState().lang || 'zh'
  const val = dicts[lang][key] ?? dicts['zh'][key]
  if (Array.isArray(val)) return val
  return []
}

export function useT() {
  const lang = useStore(s => s.lang)
  return { t, tArray, lang }
}
